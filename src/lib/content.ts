export const articles: Article[] = allArticleSeeds.map((seed, index) => {
  const previous = allArticleSeeds[(index - 1 + allArticleSeeds.length) % allArticleSeeds.length];
  const next = allArticleSeeds[(index + 1) % allArticleSeeds.length];
  const resolvedCategory = mapExpandedCategory(seed);
  const sameCategory = allArticleSeeds.find((item, itemIndex) => itemIndex !== index && mapExpandedCategory(item) === resolvedCategory);

  const override = {
    ...(articleOverrides[seed.slug] ?? {}),
    ...(strictArticleOverrides[seed.slug] ?? {}),
  };
  const strictQuiz = strictQuizBySlug[seed.slug];

  return {
    title: seed.title,
    slug: seed.slug,
    description: override?.description ?? priorityDescription(seed),
    date: "2026-05-01",
    updated: override?.updated ?? "2026-05-01",
    author: "Редакция RunPsy",
    category: resolvedCategory,
    tags: seed.tags,
    readingTime: override?.readingTime ?? priorityReadingTime(seed.priority),
    seoTitle: override?.seoTitle ?? `${seed.title}: что делать без паники`,
    seoDescription: override?.seoDescription ?? priorityDescription(seed),
    status: "published",
    intent: seed.intent,
    cta:
      override?.cta ??
      "Если в вашей ситуации много тревоги, боли или повторяющихся конфликтов, начните с диагностики: разложите факты, чувства и ближайший безопасный шаг.",
    safetyNote: seed.safetyNote,
    introduction: override?.introduction,
    relatedSlugs:
      override?.relatedSlugs ??
      (Array.from(new Set([next.slug, previous.slug, sameCategory?.slug].filter(Boolean))) as string[]),
    insightImpulse: makeInsightImpulse(seed),
    sections: override?.sections ?? makeSections(seed),
    faq: override?.faq ?? makeFaq(seed),
    quiz: strictQuiz ?? override?.quiz ?? makeQuiz(seed),
  };
});

