export function getPublishedArticles() {
  return articles.filter((article) => article.status === "published");
}

export function getArticleBySlug(slug: string) {
  return articles.find((article) => article.slug === slug);
}

export function getCategoryBySlug(slug: string) {
  return categories.find((category) => category.slug === slug);
}

export function getAuthorBySlug(slug: string) {
  return authors.find((author) => author.slug === slug);
}

export function getRelatedArticles(slugs: string[]) {
  return getPublishedArticles().filter((article) => slugs.includes(article.slug));
}

export function getArticlesByCategory(slug: string) {
  return getPublishedArticles().filter((article) => article.category === slug);
}
