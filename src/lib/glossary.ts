const aliasEntries = glossaryEntries.flatMap((entry) => entry.aliases.map((alias) => ({ alias, entry })));
const sortedAliases = aliasEntries.sort((a, b) => b.alias.length - a.alias.length);
const escapedAliases = sortedAliases.map(({ alias }) => alias.replace(/[.*+?^${}()|[\]\\]/g, "\\$&"));

export const glossaryTermPattern = new RegExp(`(?<![\\p{L}\\p{N}])(${escapedAliases.join("|")})(?![\\p{L}\\p{N}])`, "giu");

export function getGlossaryEntryByMatch(match: string) {
  const lowered = match.toLocaleLowerCase("ru");
  return sortedAliases.find(({ alias }) => alias.toLocaleLowerCase("ru") === lowered)?.entry;
}

export function getGlossaryEntryBySlug(slug: string) {
  return glossaryEntries.find((entry) => entry.slug === slug);
}
