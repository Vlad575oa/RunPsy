    introduction=EXCLUDED.introduction, insight_impulse=EXCLUDED.insight_impulse,
    sections=EXCLUDED.sections, faq=EXCLUDED.faq, quiz=EXCLUDED.quiz,
    sort_order=EXCLUDED.sort_order
