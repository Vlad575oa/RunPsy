async function main() {
  loadLocalEnv();
  const client = new Client({ connectionString: process.env.DATABASE_URL });
  await client.connect();
  for (const a of articles) {
    await client.query(INSERT_SQL, [
      a.slug, a.title, a.description, a.date, a.author_slug, a.category_slug,
      JSON.stringify(a.tags), a.reading_time, a.seo_title, a.seo_description, a.status, a.intent,
      a.cta, a.safety_note, JSON.stringify(a.related_slugs), a.introduction,
      JSON.stringify(a.insight_impulse), JSON.stringify(a.sections), JSON.stringify(a.faq),
      JSON.stringify(a.quiz), a.sort_order,
    ]);
    console.log(`✓ ${a.slug}`);
  }
  await client.end();
  console.log("Done.");
}

main().catch((e) => { console.error(e); process.exit(1); });
